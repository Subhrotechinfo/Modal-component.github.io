$(document).ready(function (){
			
		$("#modalId").css("display","none");

		$(".w3-button").click(() => {
    		$("#modalId").css("display","block");
		});

		$("#close").click(() => {
    		$("#modalId").css("display","none");
		});
		$(".close").click(() => {
    		$("#modalId").css("display","none");
		});

		 
});